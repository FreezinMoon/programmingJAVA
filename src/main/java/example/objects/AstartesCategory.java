package example.objects;

/**
 * The AstartesCategory enum represents the various categories of Astartes.
 */
public enum AstartesCategory {
    /**
     * Represents an Assault Astartes.
     */
    ASSAULT,

    /**
     * Represents an Inceptor Astartes.
     */
    INCEPTOR,

    /**
     * Represents a Chaplain Astartes.
     */
    CHAPLAIN,

    /**
     * Represents an Apothecary Astartes.
     */
    APOTHECARY
}